/**
 * Правильное склонение существительных в русском языке.
 *
 * @example
 * pluralize(0, 'поездка', 'поездки', 'поездок') // "поездок"
 * pluralize(1, 'поездка', 'поездки', 'поездок') // "поездка"
 * pluralize(2, 'поездка', 'поездки', 'поездок') // "поездки"
 * pluralize(5, 'поездка', 'поездки', 'поездок') // "поездок"
 * pluralize(21, 'поездка', 'поездки', 'поездок') // "поездка"
 */
export function pluralize(n: number, one: string, few: string, many: string): string {
  const mod10 = n % 10;
  const mod100 = n % 100;

  // n%10==1 && n%100!=11 → одна форма (1, 21, 31, ...)
  if (mod10 === 1 && mod100 !== 11) {
    return one;
  }

  // n%10 in [2,3,4] && n%100 not in [12,13,14] → несколько форма (2, 3, 4, 22, 23, 24, ...)
  if (mod10 >= 2 && mod10 <= 4 && !(mod100 >= 12 && mod100 <= 14)) {
    return few;
  }

  // иначе (0, 5-20, 25-30, ...) → много форма
  return many;
}
